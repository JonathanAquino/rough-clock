def get_letter(letter):
    """
    Returns a string for a big ASCII letter.
    """
    def extract_letter(start_col, end_col):
        lines = letters.splitlines()[1:]
        output_lines = [line[start_col:end_col] for line in lines]
        return '\n'.join(output_lines)
    if letter == 'A':
        return extract_letter(0, 16)
    if letter == 'B':
        return extract_letter(16, 34)
    if letter == 'C':
        return extract_letter(34, 50)
    if letter == 'D':
        return extract_letter(50, 66)
    if letter == 'E':
        return extract_letter(66, 82)
    if letter == 'F':
        return extract_letter(82, 98)
    if letter == 'G':
        return extract_letter(98, 114)
    if letter == 'H':
        return extract_letter(114, 130)

